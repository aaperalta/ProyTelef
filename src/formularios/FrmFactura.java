private void BT_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BT_agregarActionPerformed
   
    
      try{
        int id_telefono = Integer.parseInt(((String)this.cb_producto.getSelectedItem()).split("\\|")[0].trim());    
        Telefono  t      = Telefono.obtenerProductoPorId(this.telefonos, id_telefono);
        int cantidad    = Integer.parseInt(this.txt_cantidad.getText());        
        DefaultTableModel tb = (DefaultTableModel)tb_productos.getModel();
        
        int fila =tb.getRowCount();
        tb.insertRow(fila,new Object[]{cantidad,t.getId_telefono(),t.getMarca(),t.getPrecio(),t.getPrecio()*cantidad}); 
        
        float l = t.getPrecio()*cantidad;
        auy_num+=l;
        txt_subtotal.setText(String.valueOf(auy_num));
        iva = Math.rint((auy_num * 0.12)*100)/100;
        txt_iva.setText(String.valueOf(iva));
        total = Math.rint((auy_num + iva)*100)/100;
        txt_total.setText(String.valueOf(total));
        
        
        Detalle_Factura detalle = new Detalle_Factura();    
      
        detalle.setId_telefono(id_telefono);        
        detalle.setCantidad(cantidad);
        detalle.setPrecio(t.getPrecio());
        detalle.setTotal(l); 
        
        tabla_listado.add(detalle);
        
        
    }catch(Exception e){
        System.out.println(e.toString());
        JOptionPane.showMessageDialog(rootPane,"DEBE INGRESAR UNA CANTIDAD", "", JOptionPane.INFORMATION_MESSAGE);                    
    }    
    
    
}//GEN-LAST:event_BT_agregarActionPerformed

