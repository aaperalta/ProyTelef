  public void cargarTelefonos(){
      
      
        Conexion c = new Conexion();
        try{
            c.conectar();
            ArrayList resultado = c.consultarTelefonos();
            this.telefonos = resultado;
            String[] telefonos = new String[resultado.size()];
            for(int i=0; i<resultado.size(); i++){
                
                Telefono t = (Telefono)resultado.get(i);
                int id_telefono     = t.getId_telefono();
                String marca       = t.getMarca();
                String modelo       = t.getModelo();
                String descripcion  = t.getDescripcion(); 
                float precio        = t.getPrecio();    
                telefonos[i]        =id_telefono+" | " +marca+" "+modelo+" "+descripcion+" | $"+precio;
                this.cb_producto.addItem(telefonos[i]);
            }            
            cb_producto.setModel(new javax.swing.DefaultComboBoxModel(telefonos));                        
        }catch(Exception e){
        
        }
    } 
      
    
    

