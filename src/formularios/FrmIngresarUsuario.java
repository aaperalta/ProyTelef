    private boolean registroValido() {

        if (this.txt_usuario.getText().trim().equals("")
                || this.txt_clave.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "DEBE LLENAR TODOS LOS CAMPOS", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        String clave = this.txt_clave.getText();
        try {
            if (!(clave.length() >= 5)) {
                JOptionPane.showMessageDialog(null, "LA CONTRASEÃ‘A DEBE MAS DE 4 DIGITOS ", "ERROR", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (Exception e) {
        }
        Conexion c = new Conexion();
        String id = this.txt_usuario.getText();
        try {
            c.conectar();
            ArrayList usuario = c.consultarUsuarioporid();

            for (int i = 0; i < usuario.size(); i++) {
                Usuario u = (Usuario) c.consultarUsuarioporid().get(i);
                if (u.getId_usuario().equals(id)) {
                    JOptionPane.showMessageDialog(null, "EL USUARIO YA SE ENCUENTRA REGISTRADO, ELIJA OTRO ID", "ERROR", JOptionPane.ERROR_MESSAGE);
                    return false;
                }
            }
        } catch (Exception e) {
        }
        return true;
    }

