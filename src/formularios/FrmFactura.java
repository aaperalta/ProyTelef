private void BT_ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BT_ingresarActionPerformed
// TODO add your handling code here:           
    
    
    String codigo    = this.txt_codigo.getText();
    int id_cliente  = Integer.parseInt(this.txt_idcliente.getText());   
    
    float Subtotal = Float.parseFloat(this.txt_subtotal.getText());
    float iva = Float.parseFloat(this.txt_iva.getText());
    float total = Float.parseFloat(this.txt_total.getText());
    int cantidad = Integer.parseInt(this.txt_cantidad.getText());

    
    
    Conexion c = new Conexion();
    
    try{        
       
        c.conectar();
        
        Factura f = new Factura();
        f.setCodigo(codigo);
        f.setId_cliente(id_cliente);
        f.setSubtotal(Subtotal);
        f.setIva(iva);
        f.setTotal(total);     
        if(c.procesarVenta(f,tabla_listado)){
        JOptionPane.showMessageDialog(rootPane,"SE REALIZO LA VENTA CON ÉXITO", "ÉXITO", JOptionPane.INFORMATION_MESSAGE);
         FrmFactura frm = new FrmFactura (null,null);
         frm.setVisible(true);
         this.dispose();
         
        }else{
            JOptionPane.showMessageDialog(rootPane,"DEBE INGRESAR OTRO NUMERO DE FACTURA", "ERROR", JOptionPane.INFORMATION_MESSAGE);                    
        }
        
    }catch(Exception e){
    JOptionPane.showMessageDialog(rootPane,"OCURRIÓ UN ERROR EN LA CONEXIÓN", "ERROR", JOptionPane.ERROR_MESSAGE);                    
    }     
    c.desconectar();
      
}//GEN-LAST:event_BT_ingresarActionPerformed

